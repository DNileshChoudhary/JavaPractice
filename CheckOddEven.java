class CheckOddEven
{
	public static void main(String args[])
	{
		int number=5;
		{
			System.out.println("Given number is :"+number);
			if(number%2==0)
			{
				System.out.println("It is an Even number");
			}else
			{
				System.out.println("It is an Odd number");
			}System.out.println("bye!");
		} 
	}
}