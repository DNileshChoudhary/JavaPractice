class Nilesh
{
	public static void main(String args[])
	{
		double a,b;
		a=Double.parseDouble(args[0]);
		b=Double.parseDouble(args[1]);
		System.out.println( a+ "+" +b+ "=" +(a+b) );
		System.out.println( a+ "-" +b+ "=" +(a-b) );
		System.out.println( a+ "*" +b+ "=" +(a*b) );
		System.out.println( a+ "/" +b+ "=" +(a/b) );
		System.out.println( a+ "%" +b+ "=" +(a%b) );
	}
}