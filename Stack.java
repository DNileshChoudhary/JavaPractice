/*
Stack
1.Stack is a Linear data Structure.
2.It follows LIFO(Last In First Out) principle.
3.Operation on Stack
	a.push 
	b.pop
	c.peek()
	c.isFull()
	e.isEmpty
=>It maintanis a pointer called Top. 
=>It always points to top of stack.
=>push and pop always takes place at top of stack.

	push = It means inserting an element into the stack and top incremented.

	pop = It means deleting an element an element from the stack and top decremented.

	peek = It gives the top most element of the Stack.

	isFull() = It returns true if stack is full else return false.

	isEmpty() = It returns true if stack is empty else returns false.

=>Every stack maintains max Capacity.

=>Standard Error messages:
	a.Stack overflow
	b.Stack Underflow

Applications of Stack : 
------------------------------
1.Infix expression to postfix(Expression conversion).
2.Postfix expression evaluation.
3.Recursion 
4.Parameter Passing through function.
5. DFS(Depth First Search) traversal.
*/

